public class Agente {

    private int idAgente;
    private String nomeAgente;
    private String emailAgente;
    private String numMatAgente;


    public int getIdAgente() {
        return idAgente;
    }

    public void setIdAgente(int idAgente) {
        this.idAgente = idAgente;
    }

    public String getNomeAgente() {
        return nomeAgente;
    }

    public void setNomeAgente(String nomeAgente) {
        this.nomeAgente = nomeAgente;
    }

    public String getEmailAgente() {
        return emailAgente;
    }

    public void setEmailAgente(String emailAgente) {
        this.emailAgente = emailAgente;
    }

    public String getNumMatAgente() {
        return numMatAgente;
    }

    public void setNumMatAgente(String numMatAgente) {
        this.numMatAgente = numMatAgente;
    }
}
